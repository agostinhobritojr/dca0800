#include <stdio.h>

int main(){
  float x[4][4];
  int i, j;
  x[0][0] = 3;
  x[0][1] = 4;
  x[0][2] = 1;
  x[0][3] = 2;
  x[1][0] = 0;
  x[1][1] = -3;
  x[1][2] = 9;
  x[1][3] = 8;
  x[2][0] = 0;
  x[2][1] = 0;
  x[2][2] = 4;
  x[2][3] = 5;
  x[3][0] = 1;
  x[3][1] = 1;
  x[3][2] = 2;
  x[3][3] = 6;

  for(i=0; i<4; i++){
    for(j=0; j<4; j++){
      printf("%4.1f ", x[i][j]);
    }
    printf("\n");
  }
  return 0;
}
